var input = prompt("Enter your mark of 1 subject")


if(input>100){

document.write("Invalid mark")

}


else if(input>=80){

document.write("A+")

}

else if(input>=70){

document.write("A")

}

else if(input>=60){

document.write("A-")

}

else if(input>=50){

document.write("B")

}

else if(input>=40){

document.write("C")

}

else if(input>=33){

document.write("D")

}
else if(input>=0){

document.write("Fail")

}

else{
    document.write("Invalid input")
}







